/*      While       */

let i=0;
while(i<=10){
    console.log(`Value of i is ${i}`)
    i+=2
}

let myArray = ['a','b','c','d','e','f']
let index =0
while(index<myArray.length){
    console.log(`In myArray element at index ${index} is ${myArray[index]} `)
    index++
}


/*    do while     */
let score = 1
do{
    console.log(`The score is ${score}`)
    score+=10
}while(score>100);
